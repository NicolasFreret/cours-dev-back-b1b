				<h2 class="h2">Culture</h2>
				<p class="m-green">Notre spiruline croît dans 600 m2 de bassins fermés protégés par une serre en verre. Le milieu de culture est enrichi ponctuellement de nutriments : du sel (10g/L environ), du fer, du potassium, du magnésium et du phosphore pour favoriser une bonne photosynthèse. De l’azote est ajouté – en fonction des besoins de la spiruline – pour assurer son bon développement (fabrication des protéines). Le Ph de la culture de spiruline est compris entre 9,6 et 10,6.</p>
				<p>Les bassins sont agités en continue par des roues à aube. L’agitation est essentielle pour optimiser les échanges gazeux effectués par la spiruline (absorption C02 et élimination d’O2) ainsi que pour favoriser la couverture lumineuse de toute la spiruline.</p>
				<p>La production de spiruline est saisonnière. La croissance de la spiruline est optimale de mars à fin octobre. En dehors de cette période la spiruline rentre dans une forme de « dormance » sa croissance est trop faible pour pouvoir la récolter.</p>
