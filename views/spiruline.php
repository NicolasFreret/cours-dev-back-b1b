			<h2 class="h2">Origine et biotope</h2>
			<p class="m-green">La spiruline, dont l’espèce principale est Arthrospira Platensis, est une cyanobactérie comestible utilisée comme aliment de complément en raison de sa composition nutritionnelle riche et variée.</p>
			<p>Appelée communément microalgue, c’est un micro-organisme (250 µm de longueur !) qui croît par division binaire (1 filament va donner 2 filaments) à activité photosynthétique. La spiruline va utiliser l’énergie lumineuse (le soleil) et la convertir pour se développer. Elle va alors, entre autres, pouvoir absorber du C02 atmosphérique et relâcher de l’oxygène grâce à la chlorophylle, l’un de ses nombreux pigments.</p>
			<p>A l’origine, elle croît dans des milieux aqueux saumâtres, alcalins et riches en nutriments des zones tropicales et semi tropicales du monde. Elle va se développer lorsque les conditions environnementales lui sont favorables (température idéale de croissance : 35°C). Sa forte plasticité écologique explique la présence de la spiruline aussi bien dans des lacs alcalins en Afrique (Tchad) qu’en Asie (Inde) ou en Amérique latine.</p>
			<p>En France, la spiruline se cultive en bassins fermés depuis les années 2000 avec les travaux de recherche initiés par le chimiste Jean-Paul Jourdan dans les années 70.</p>
